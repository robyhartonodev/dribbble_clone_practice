class FoodItem {
  final double ratings;
  final String name;
  final String type;
  final String icon;
  final String description;
  final double price;
  final String image;

  FoodItem({
    required this.ratings,
    required this.name,
    required this.type,
    required this.icon,
    required this.description,
    required this.price,
    required this.image
  });
}